#include <etaos/kernel.h>
#include <etaos/types.h>
#include <etaos/irq.h>

void irq_save_and_disable(unsigned long *flags)
{
	*flags = arch_irq_get_flags();
	arch_irq_disable();
}

void irq_restore(unsigned long *flags)
{
	if(!(*flags))
		return;
	arch_irq_restore_flags(flags);
}

